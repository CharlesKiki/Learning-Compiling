DATA SEGMENT
INMESS DB 'PLEASE INPUT A STRING(LENGTH<9):$'
LENMESS DB 10,13,'THE LENGTH OPF THE STRING IS:$'
CHARMESS DB 10,13,'THE SECOND CHAR OF THE STRING IS:$'
INSTRING DB 10,?,10 DUP(?)
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX

LEA DX,INMESS
MOV AH,09H
INT 21H

LEA DX,INSTRING
MOV AH,0AH
INT 21H

LEA DX ,LENMESS
MOV AH,09H
INT 21H

LEA BX,INSTRING
INC BX 
MOV DL,[BX]
ADD DL,30H
MOV AH,02H
INT 21H
;Length
LEA DX,CHARMESS
MOV AH,09H
INT 21H

MOV DL,[BX+2]
MOV AH,02H
INT 21H

MOV AX,4C00H
INT 21H

CODE ENDS
END START
